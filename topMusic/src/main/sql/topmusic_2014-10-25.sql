CREATE TABLE `Album` (
  `_id` long NOT NULL,
  `artistID` text DEFAULT NULL,
  `image` text DEFAULT NULL,
  `thumbnail` text DEFAULT NULL,
  `name` text DEFAULT NULL,
  `publishYear` text DEFAULT NULL,
  `webID` text DEFAULT NULL,
  `artistName`	TEXT DEFAULT 'NULL',
  PRIMARY KEY (`_id`)
) ;

CREATE TABLE `Artist` (
  `_id` long NOT NULL,
  `image` text DEFAULT NULL,
  `thumbnail` text DEFAULT NULL,
  `name` text DEFAULT NULL,
  `webID` text DEFAULT NULL,
  PRIMARY KEY (`_id`)
) ;
CREATE TABLE `Song` (
  `_id` long NOT NULL,
  `albumID` long DEFAULT NULL,
  `artistID` long DEFAULT NULL,
  `genre` text DEFAULT NULL,
  `image` text DEFAULT NULL,
  `thumbnail` text DEFAULT NULL,
  `name` text DEFAULT NULL,
  `albumName` text DEFAULT NULL,
  `artistName` text DEFAULT NULL,
  `runLength` text DEFAULT NULL,
  `url` text DEFAULT NULL,
  `webID` text DEFAULT NULL,
  PRIMARY KEY (`_id`)
) ;

CREATE TABLE IF NOT EXISTS Genre (
    _id int NOT NULL PRIMARY KEY,
    parentID int NOT NULL DEFAULT 0,
    name text NOT NULL,
    zhName text NOT NULL
  );
  CREATE INDEX genre_name ON Genre (zhName);

CREATE TABLE `Playlist` (
	`_id`	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	`name`	text DEFAULT NULL UNIQUE
);
CREATE INDEX playlist_name ON Playlist (name);

CREATE TABLE `SongListAssoc` (
  `playlist` long NOT NULL,
  `song` long NOT NULL,
  `play_order` int DEFAULT 0,
  PRIMARY KEY (`playlist`, `song`)
) ;
CREATE INDEX songlist_order ON `SongListAssoc`(`play_order`);

